def pr10():
    for i in range(0,11):
        print(i,end=" ")
    print()

def prNums(n):
    for i in range(0,n+1):
        print(i,end=" ")
    print()

def test():
    prNums(15)
    pr10()

def pr5():
    i=0
    while i<=10:
        print(i,end=" ")
        i += 1
    print()

def test():
    pr5()
    pr10()
    prNums

test()